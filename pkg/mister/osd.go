// ShowSplash renders a notification to the framebuffer synchronously.
// This must be called while the Menu core is active (or transitioning)
// so the framebuffer is visible on screen.
func (o *OSD) ShowSplash(title, subtitle string) {
	o.mu.Lock()
	defer o.mu.Unlock()
	// Re-read FB size since it changes when switching cores
	w, h := readFBSize()
	o.width = w
	o.height = h
	o.stride = w * fbBPP
	o.buf = make([]byte, w*h*fbBPP)
	o.renderNotification(title, subtitle)
	o.flush()
}

func (o *OSD) renderNotification(title, subtitle string) {
	o.Clear()

	padding := 20
	branding := "MisterClaw"

	// Calculate box dimensions
	titleScale := 2
	subtitleScale := 1
	brandingScale := 1

	titleW := textWidth(title, titleScale)
	subtitleW := textWidth(subtitle, subtitleScale)
	brandingW := textWidth(branding, brandingScale)

	// Box width: ~60% of screen, but at least wide enough for content
	boxW := o.width * 60 / 100
	contentMax := titleW
	if subtitleW > contentMax {
		contentMax = subtitleW
	}
	if brandingW > contentMax {
		contentMax = brandingW
	}
	if contentMax+padding*2 > boxW {
		boxW = contentMax + padding*2
	}
	if boxW > o.width-40 {
		boxW = o.width - 40
	}

	titleH := fontHeight * titleScale
	subtitleH := fontHeight * subtitleScale
	brandingH := fontHeight * brandingScale
	spacing := 8

	boxH := padding + titleH + spacing + subtitleH + spacing + brandingH + padding

	// Position: bottom-center
	boxX := (o.width - boxW) / 2
	boxY := o.height - boxH - 60

	// Draw background
	o.FillRect(boxX, boxY, boxW, boxH, colorBoxBG)

	// Draw title (centered, 2x scale)
	titleX := boxX + (boxW-titleW)/2
	titleY := boxY + padding
	o.drawTextScaled(titleX, titleY, title, colorWhite, titleScale)

	// Draw subtitle (centered, 1x scale)
	subtitleX := boxX + (boxW-subtitleW)/2
	subtitleY := titleY + titleH + spacing
	o.DrawText(subtitleX, subtitleY, subtitle, colorLightGray)

	// Draw branding (centered, 1x scale)
	brandingX := boxX + (boxW-brandingW)/2
	brandingY := subtitleY + subtitleH + spacing
	o.DrawText(brandingX, brandingY, branding, colorDimGray)
}

// FormatNotificationText returns the lines that would be rendered (for testing).
func FormatNotificationText(title, subtitle string) []string {
	return []string{title, subtitle, "MisterClaw"}
}

