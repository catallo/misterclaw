// Framebuffer constants for MiSTer.
const (
	fbBPP = 4 // 32-bit BGRA
)

// fbSizeFile is the sysfs path for reading framebuffer resolution; tests override this.
var fbSizeFile = "/sys/class/graphics/fb0/virtual_size"

// readFBSize reads the current framebuffer resolution from sysfs.
// Returns (width, height); falls back to 640x480 on error.
func readFBSize() (int, int) {
	data, err := os.ReadFile(fbSizeFile)
	if err != nil {
		return 640, 480
	}
	var w, h int
	if _, err := fmt.Sscanf(strings.TrimSpace(string(data)), "%d,%d", &w, &h); err != nil || w <= 0 || h <= 0 {
		return 640, 480
	}
	return w, h
}

// Font constants (8x16 monospace).
const (
	fontWidth  = 8
	fontHeight = 16
)

// fbDevice is the framebuffer path; tests override this.
var fbDevice = "/dev/fb0"

// Color represents a BGRA pixel.
type Color struct {
	B, G, R, A uint8
}

var (
	colorWhite    = Color{0xFF, 0xFF, 0xFF, 0xFF}
	colorLightGray = Color{0xCC, 0xCC, 0xCC, 0xFF}
	colorDimGray  = Color{0x80, 0x80, 0x80, 0xFF}
	colorBoxBG    = Color{0x1E, 0x14, 0x14, 0xCC} // BGRA: B=30, G=20, R=20, A=0xCC
)

// OSD manages the on-screen display overlay.
type OSD struct {
	mu     sync.Mutex
	buf    []byte
	width  int
	height int
	stride int
	cancel chan struct{} // signals current notification to stop
}

var globalOSD *OSD
var osdOnce sync.Once

// GetOSD returns the singleton OSD instance.
func GetOSD() *OSD {
	osdOnce.Do(func() {
		w, h := readFBSize()
		globalOSD = &OSD{
			width:  w,
			height: h,
			stride: w * fbBPP,
			buf:    make([]byte, w*h*fbBPP),
		}
	})
	return globalOSD
}

// newOSD creates an OSD with the given dimensions (for testing).
func newOSD(width, height int) *OSD {
	return &OSD{
		width:  width,
		height: height,
		stride: width * fbBPP,
		buf:    make([]byte, width*height*fbBPP),
	}
}

// Clear fills the framebuffer buffer with transparent black.
func (o *OSD) Clear() {
	for i := range o.buf {
		o.buf[i] = 0
	}
}

// FillRect draws a filled rectangle in BGRA format.
func (o *OSD) FillRect(x, y, w, h int, c Color) {
	for row := y; row < y+h; row++ {
		if row < 0 || row >= o.height {
			continue
		}
		for col := x; col < x+w; col++ {
			if col < 0 || col >= o.width {
				continue
			}
			off := row*o.stride + col*fbBPP
			o.buf[off+0] = c.B
			o.buf[off+1] = c.G
			o.buf[off+2] = c.R
			o.buf[off+3] = c.A
		}
	}
}

// DrawText renders a string using the embedded 8x16 font at 1x scale.
func (o *OSD) DrawText(x, y int, text string, c Color) {
	o.drawTextScaled(x, y, text, c, 1)
}

// drawTextScaled renders text at the given integer scale factor.
func (o *OSD) drawTextScaled(x, y int, text string, c Color, scale int) {
	cx := x
	for _, ch := range text {
		if ch < 32 || ch > 126 {
			ch = '?'
		}
		glyph := font8x16[ch-32]
		for row := 0; row < fontHeight; row++ {
			bits := glyph[row]
			for col := 0; col < fontWidth; col++ {
				if bits&(0x80>>uint(col)) != 0 {
					// Draw a scale x scale block for each pixel
					for sy := 0; sy < scale; sy++ {
						for sx := 0; sx < scale; sx++ {
							px := cx + col*scale + sx
							py := y + row*scale + sy
							if px >= 0 && px < o.width && py >= 0 && py < o.height {
								off := py*o.stride + px*fbBPP
								o.buf[off+0] = c.B
								o.buf[off+1] = c.G
								o.buf[off+2] = c.R
								o.buf[off+3] = c.A
							}
						}
					}
				}
			}
		}
		cx += fontWidth * scale
	}
}

// textWidth returns the pixel width of a string at the given scale.
func textWidth(text string, scale int) int {
	return len(text) * fontWidth * scale
}

// flush writes the buffer to the framebuffer device.
func (o *OSD) flush() error {
	f, err := os.OpenFile(fbDevice, os.O_WRONLY, 0)
	if err != nil {
		return err
	}
	defer f.Close()
	_, err = f.Write(o.buf)
	return err
}

// ShowNotification displays a styled notification box and auto-clears after durationMs.
// It is goroutine-safe; calling it while a notification is active cancels the previous one.
func (o *OSD) ShowNotification(title, subtitle string, durationMs int) {
	o.mu.Lock()

	// Cancel any previous notification timer
	if o.cancel != nil {
		close(o.cancel)
	}
	cancelCh := make(chan struct{})
	o.cancel = cancelCh

	// Re-read FB size since it changes when switching cores
	w, h := readFBSize()
	o.width = w
	o.height = h
	o.stride = w * fbBPP
	o.buf = make([]byte, w*h*fbBPP)

	// Draw the notification
	o.renderNotification(title, subtitle)
	err := o.flush()

	o.mu.Unlock()

	if err != nil {
		// Graceful degradation: can't write to fb, silently skip
		return
	}

	// Auto-clear after duration in a background goroutine
	go func() {
		select {
		case <-time.After(time.Duration(durationMs) * time.Millisecond):
			o.mu.Lock()
			defer o.mu.Unlock()
			// Only clear if we haven't been cancelled (replaced by another notification)
			select {
			case <-cancelCh:
				return
			default:
			}
			o.Clear()
			o.flush() //nolint: errcheck
		case <-cancelCh:
			return
		}
	}()
}

